let currentLang = 'ru';

function setLang(lang) {
  currentLang = lang;
  document.documentElement.setAttribute('data-lang', lang);
}

function _(key) {
  return (LANG[currentLang] && LANG[currentLang][key]) || (LANG.ru[key]) || key;
}

function getLang() {
  return currentLang;
}
