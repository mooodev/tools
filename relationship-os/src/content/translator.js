/**
 * Get translator pair for a given week.
 * @param {number} weekIndex
 * @returns {{ guy: Object, girl: Object }}
 */
function getTranslatorForWeek(weekIndex) {
  const idx = weekIndex % TRANSLATOR_GUY.length;
  return {
    guy: TRANSLATOR_GUY[idx],
    girl: TRANSLATOR_GIRL[idx],
  };
}

function formatTranslatorMessage(entry) {
  return `🔄 *Переводчик недели*\n\n${entry.original}\n\n💡 ${entry.translation}`;
}

